class UserEntity {
  final int id;
  final String name;
  final String email;
  final String status;
  final String role;
  final String avatar;
  final String transmission;

  UserEntity({
    required this.id,
    required this.name,
    required this.email,
    required this.status,
    required this.role,
    required this.avatar,
    required this.transmission,
  });
}
